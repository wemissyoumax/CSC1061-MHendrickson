package com.maxwell.oop;

public class Wheel {
	private String material;
	private int radius;
	
	public Wheel(String material, int radius) {
		super();
		this.material = material;
		this.radius = radius;
	}

	public String getMaterial() {
		return material;
	}

	public int getRadius() {
		return radius;
	}
	
	
}
